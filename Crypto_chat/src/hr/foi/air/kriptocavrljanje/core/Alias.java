package hr.foi.air.kriptocavrljanje.core;

public class Alias {

	private String hashId;
	private String alias;
	
	public Alias() {
		
	}
	
	public String getHashId() {
		return hashId;
	}
	public void setHashId(String hashId) {
		this.hashId = hashId;
	}
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
}
